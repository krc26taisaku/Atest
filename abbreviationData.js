window.KenteiAbbreviation=(()=>{
  const dictionary=window.KENTEI_ABBREVIATIONS||{};

  function escapeRegExp(value){
    return String(value).replace(/[.*+?^${}()|[\]\\]/g,'\\$&');
  }

  function findEntries(word){
    const source=String(word||'');
    const entries=[];
    Object.entries(dictionary)
      .sort((a,b)=>b[0].length-a[0].length)
      .forEach(([key,[full,japanese]])=>{
        const pattern=new RegExp(`(^|[^A-Za-z0-9])${escapeRegExp(key)}(?=$|[^A-Za-z0-9])`,'i');
        if(pattern.test(source)){
          entries.push({key,full,japanese});
        }
      });

    // 単語自体が括弧付きで正式名称を含む場合
    const match=source.match(/^([A-Za-z][A-Za-z0-9+./-]*)\s*\(([^)]+)\)/);
    if(match&&!entries.some(item=>item.key.toLowerCase()===match[1].toLowerCase())){
      entries.push({key:match[1],full:match[2],japanese:''});
    }
    return entries;
  }

  function letters(full){
    const words=String(full||'')
      .split(/[\s,/()-]+/)
      .filter(word=>word&&/^[A-Za-z]/.test(word));
    return words.map(word=>`${word[0].toUpperCase()} = ${word}`).join(' / ');
  }

  function display(word){
    const source=String(word||'');
    const entries=findEntries(source);
    if(!entries.length)return source;
    const primary=entries[0];
    const clean=source.replace(/\s*\([^)]*\)\s*$/,'').trim();
    return `${clean}（${primary.full}${primary.japanese?`／${primary.japanese}`:''}）`;
  }

  function details(word){
    const entries=findEntries(word);
    if(!entries.length)return '';
    return entries.map(item=>{
      const breakdown=letters(item.full);
      return [
        `${item.key} = ${item.full}`,
        item.japanese,
        breakdown
      ].filter(Boolean).join('\n');
    }).join('\n\n');
  }

  function searchText(question){
    const entries=findEntries(question?.word);
    return [
      question?.word,
      question?.meaning,
      ...entries.flatMap(item=>[item.key,item.full,item.japanese,letters(item.full)])
    ].filter(Boolean).join(' ').toLowerCase();
  }

  function acceptedAnswers(question){
    const answers=[String(question?.word||'').trim()];
    for(const entry of findEntries(question?.word)){
      answers.push(entry.key,entry.full,entry.japanese);
    }
    return [...new Set(answers.filter(Boolean))];
  }

  function normalizeAnswer(value){
    return String(value||'')
      .normalize('NFKC')
      .toLowerCase()
      .replace(/\s+/g,'')
      .replace(/[。、，．・：:；;（）()「」『』【】［］\[\]<>＜＞!！?？"'`´’‘〜～\-_/\\]/g,'');
  }

  function matchesInput(input,question){
    const entered=normalizeAnswer(input);
    if(!entered)return false;
    return acceptedAnswers(question).some(answer=>normalizeAnswer(answer)===entered);
  }

  function enrich(question){
    let entries=findEntries(question.word);

    // Explicit English aliases from the original parenthetical notation.
    for(const alias of question.aliases||[]){
      if(/^[A-Za-z][A-Za-z0-9\s+./,&-]{1,100}$/.test(alias)){
        const token=String(question.word||'').match(/[A-Za-z][A-Za-z0-9+./-]*/)?.[0]||question.word;
        if(!entries.some(item=>item.full.toLowerCase()===alias.toLowerCase())){
          entries.push({key:token,full:alias,japanese:''});
        }
      }
    }

    // Only dictionary entries or explicit English aliases are expanded.
    // Unknown English-looking words remain unchanged instead of using the
    // entire explanation as a fake Japanese name.
    const displayWord=entries.length
      ?`${question.word}（${entries[0].full}${entries[0].japanese?`／${entries[0].japanese}`:''}）`
      :question.word;

    const abbreviationDetails=entries.map(item=>{
      const breakdown=letters(item.full);
      return[
        `${item.key} = ${item.full}`,
        item.japanese,
        breakdown
      ].filter(Boolean).join('\n');
    }).join('\n\n');

    const enriched={
      ...question,
      abbreviationEntries:entries,
      displayWord,
      abbreviationDetails
    };
    return{
      ...enriched,
      searchText:searchText(enriched)+' '+(question.aliases||[]).join(' ')
    };
  }

  window.WORD_QUESTIONS=(window.WORD_QUESTIONS||[]).map(enrich);
  return{dictionary,findEntries,letters,display,details,searchText,acceptedAnswers,matchesInput,enrich};
})();
